package com.biz.list.array;

import java.util.ArrayList;
import java.util.List;

public class List_01 {

	public static void main(String[] args) {

		List<Integer> nums; // 정수들을 담을 nums 리스트를 선언
		
		nums = new ArrayList<Integer>(); // nums 리스트를 사용할 수 있도록 생성 초기화
		
		// add() 를 이용해서 값을 추가하면 된다.
		nums.add(100);
		nums.add(5);
		nums.add(1);
		
		
		
	}

}
